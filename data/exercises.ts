export const getExerciseById = (id: string): Exercise | undefined => {
  return exercises[id];
};

export const getExercisesByIds = (ids: string[]): Exercise[] => {
  return ids.map(id => exercises[id]).filter(Boolean);
};

export const getExercisesByDifficulty = (difficulty: 'beginner' | 'intermediate'): Exercise[] => {
  return Object.values(exercises).filter(e => e.difficulty === difficulty);
};

export const getBeginnerSafeExercises = (): Exercise[] => {
  return Object.values(exercises).filter(e =>
    e.difficulty === 'beginner' &&
    !e.safetyFlags.isExplosive &&
    !e.safetyFlags.highImpact
  );
};
